    /**
     * Findet eine Region nach Namen
     */
    fun findRegionByName(name: String): Region? {
        return ALL_REGIONS.find { it.name == name }
    }
    
    /**
     * Gibt alle Kräuter zurück, die in der Region vorkommen und in der Landschaft wachsen
     */
    fun getAvailableHerbs(region: Region, landscape: Landscape): List<String> {
        return region.herbs.filter { herbName ->
            val herb = InitialHerbs.findHerbByName(herbName)
            herb?.isAvailableInLandscape(landscape) == true
        }
    }
